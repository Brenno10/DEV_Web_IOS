var celcius = 23;
const fahrenheitToCelcius = celcius * (9 / 5) + 32;
console.log(`Fahrenheit para Celcius
Celsius: ${celcius}
Fahrenheit: ${fahrenheitToCelcius}`);

var fahrenheit = 92.3;
const celciusToFahrenheit = (fahrenheit - 32) * (5 / 9);
console.log(`Celcius para Fahrenheit
Fahrenheit: ${fahrenheit}
Celcius: ${celciusToFahrenheit}`);
